                    walk(torsoconnectorbase1, 0.08F, 0.1F / 2, true, 1, 0, ticks, 1F);
                    walk(torsobase1, 0.08F, 0.15F / 2, false, 1, 0, ticks, 1F);
                    walk(headbase, 0.08F, 0.05F / 2, true, 1, 0, ticks, 1F);
                    walk(upperarmR1, 0.08F, 0.05F / 2, true, 1, 0, ticks, 1F);
