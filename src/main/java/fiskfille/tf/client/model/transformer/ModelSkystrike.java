    @Override
    public void renderArmorPiece(int armorPiece)
    {
        setToInitPose();

        if (armorPiece == 0)
        {
//            GL11.glScalef(0.85F, 0.85F, 0.85F);
//            GL11.glTranslatef(0.0F, 0.05F, -0.18F);
            GL11.glRotatef(5, 1.0F, 0.0F, 0.0F);
            this.headbase.render(0.0625F);
        }
        else if (armorPiece == 1)
        {
            GL11.glTranslatef(0.0F, 0.2F, 0.0F);
            this.upperlegL1.showModel = false;
            this.upperlegR1.showModel = false;
            this.headbase.showModel = false;
            this.waistbase.render(0.0625F);
            this.upperlegL1.showModel = true;
            this.upperlegR1.showModel = true;
            this.headbase.showModel = true;
        }
        else if (armorPiece == 2)
        {
            this.feetbaseL1.showModel = false;
            this.feetbaseR1.showModel = false;
//            GL11.glTranslatef(0.0F, 0.1F, 0.0F);
            this.upperlegL1.render(0.0625F);
            this.upperlegR1.render(0.0625F);
            this.feetbaseL1.showModel = true;
            this.feetbaseR1.showModel = true;
        }
        else if (armorPiece == 3)
        {
            this.feetbaseL1.rotationPointX -= 4;
            this.feetbaseR1.rotationPointX += 4;
            GL11.glTranslatef(0.1F, 0.0F, 0.0F);
            GL11.glRotatef(2, 1.0F, 0.0F, 0.0F);
            GL11.glRotatef(-2.5F, 0.0F, 1.0F, 0.0F);
            this.feetbaseL1.render(0.0625F);
            GL11.glRotatef(5, 0.0F, 1.0F, 0.0F);
            this.feetbaseR1.render(0.0625F);
        }
    }
